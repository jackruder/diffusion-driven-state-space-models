# ---------------------------------------------------------------------------
# Convenience helper: load a Hydra YAML and return an instantiable config
# ---------------------------------------------------------------------------

def build_model(yaml_path: str) -> Any:
    """Load a Hydra-compatible YAML and return a DDSSMConf-style config.

    For backward compatibility with scripts that previously called
    ``load_config_from_files``.  The returned object can be passed to
    ``hydra_zen.instantiate(cfg)`` to construct the ``DDSSM_base`` model.

    Args:
        yaml_path: Path to the Hydra YAML configuration file.

    Returns:
        An ``OmegaConf`` DictConfig that can be passed to ``instantiate``.
    """
    from omegaconf import OmegaConf

    with open(yaml_path, "r") as f:
        cfg = OmegaConf.load(f)
    return cfg
