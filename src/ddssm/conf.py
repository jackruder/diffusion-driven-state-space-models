"""Central hydra-zen configuration: ZenStore, config groups, and top-level DDSSMConf.

This module provides:
  - A ``ZenStore`` with ``transition`` config group (``gaussian`` / ``diffusion``).
  - A ``DDSSMHyperParamsConf`` dataclass for training hyperparameters.
  - A ``DDSSMConf`` that assembles all sub-module configs into a single
    instantiable configuration (built with hydra-zen ``builds()``).
  - A ``DDSSMTrainerConf`` for trainer construction via hydra-zen ``builds()``.
  - ``build_model(yaml_path)`` – a convenience helper that loads a Hydra YAML
    and returns an instantiable config.

All module-level ``*Conf`` classes are generated by ``builds(ClassName,
populate_full_signature=True)`` which is co-located with each class in its
own file.  This file re-exports them via the ZenStore for use with Hydra
config groups.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, List

from hydra_zen import builds, make_config, ZenStore
from omegaconf import MISSING

