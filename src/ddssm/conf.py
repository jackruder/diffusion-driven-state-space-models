# ---------------------------------------------------------------------------
# Hyperparameters dataclass (mirroring the old DDSSMHyperParams Pydantic model)
# ---------------------------------------------------------------------------

@dataclass
class _REWOConf:
    D0: float = 0.1
    nu: float = 1e-3
    alpha: float = 0.99
    tau1: float = 1.0
    tau2: float = 1.0


@dataclass
class DDSSMHyperParamsConf:
    """Training hyperparameters for DDSSM.

    Mirrors the old ``DDSSMHyperParams`` Pydantic model.
    """
    S: int = 1
    ema_decay: float = 0.999
    weight_decay: float = 1e-2
    batch_size: int = 16
    grad_accum_steps: int = 4
    t_chunk: int = 16
    clip_grad_norm: float | None = None

    lambda_schedule: str = "none"  # "none" | "linear" | "cosine" | "rewo"
    lambda_start: float = 0.001
    lambda_end: float = 1.0
    lambda_warmup_steps: int = 10

    enc_lr: float = 5e-4
    dec_lr: float = 5e-4
    zinit_lr: float = 5e-4
    trans_lr: float = 5e-4

    logvar_min: float = -7.0
    logvar_max: float = 7.0

    rewo: _REWOConf = field(default_factory=_REWOConf)


# ---------------------------------------------------------------------------
# Stages dataclasses (slim versions; full logic lives in stages.py)
# ---------------------------------------------------------------------------

@dataclass
class _StageLrsConf:
    dec_lr: float = 5e-4
    zinit_lr: float = 5e-4
    trans_lr: float = 0.0


@dataclass
class _StageTrainableConf:
    decoder: bool = True
    z_init: bool = True
    transition: bool = False


@dataclass
class _StageSchedulerConf:
    warmup_steps: int = 0
    final_lr_scale: float = 1.0


@dataclass
class _LambdaRampConf:
    end: float | None = 1.0
    delay: int = 0
    steps: int | None = None


@dataclass
class StageSpecConf:
    steps: int = MISSING
    trainable: _StageTrainableConf = field(default_factory=_StageTrainableConf)
    lrs: _StageLrsConf = field(default_factory=_StageLrsConf)
    scheduler: _StageSchedulerConf = field(default_factory=_StageSchedulerConf)
    carry_diff_moments: bool = False
    lambda_ramp: _LambdaRampConf = field(default_factory=_LambdaRampConf)
    log_every: int = 10
    val_every: int = 100
    checkpoint_every: int = 1000


@dataclass
class StagesConf:
    stage_2: StageSpecConf | None = None
    stage_3: StageSpecConf | None = None
    run: List[str] = field(default_factory=lambda: ["stage_1", "stage_2", "stage_3"])


